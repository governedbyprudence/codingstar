  @override
  _MyRegisterState createState() => _MyRegisterState();
}

class _MyRegisterState extends State<MyRegister> {
  bool load = false;
  String err_mess="";
  TextEditingController _name = new TextEditingController();
  TextEditingController _email = new TextEditingController();
  TextEditingController _password = new TextEditingController();

  validate_password(value){
    RegExp regex= new RegExp( r'^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[!@#\$&*~]).{8,}$');
    if(regex.hasMatch(value))return true;
    return false;
  }
  void createUser()async{
    setState(() {
      load=true;
    });
    print("in create user");
    FirebaseAuth.instance.createUserWithEmailAndPassword(
        email: _email.text.trim(), password: _password.text.trim(),).then((value){
        FirebaseFirestore.instance.collection("users").doc(value.user?.uid).set({
          "email":_email.text,
          "type" :"user",
          "name" : _name.text,
          "subscribed" : false,
        }).then((value) async{
          await FirebaseAuth.instance.currentUser?.updateDisplayName(_name.text);
          Navigator.pushReplacementNamed(context, "home");
        }).catchError((err)=>print("register err"+err.code));
        })
        .catchError((er){
      if(er.code=="email-already-in-use"){
        setState(() {
          err_mess="The email is already in use";
        });
      }
      print(er.code+"codo");
    });
  }
